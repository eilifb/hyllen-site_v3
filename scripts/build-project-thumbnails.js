#!/usr/bin/env node
const fs = require('fs');
const path = require('path');
const { getRepoRoot } = require('./version-source');

const THUMB_WIDTH = 360;
const THUMB_WIDTH_2X = 720;
const THUMB_QUALITY = 72;
const THUMB_QUALITY_2X = 70;

const repoRoot = getRepoRoot();
const appRoot = path.join(repoRoot, 'app');
const contentDir = path.join(appRoot, 'content', 'projects');
const publicDir = path.join(appRoot, 'public');
const outputDir = path.join(publicDir, 'static', 'images', 'generated', 'project-thumbs');
const manifestPath = path.join(appRoot, 'src', 'generatedProjectThumbnails.ts');

function resolveFromApp(specifier) {
  return require.resolve(specifier, { paths: [appRoot] });
}

function readProjectFiles(dirPath) {
  if (!fs.existsSync(dirPath)) return [];
  return fs
    .readdirSync(dirPath)
    .filter((name) => name.toLowerCase().endsWith('.mdx'))
    .map((name) => path.join(dirPath, name));
}

function extractFrontmatter(raw, absPath) {
  const match = raw.match(/^---\r?\n([\s\S]*?)\r?\n---/);
  if (!match) {
    throw new Error(`Missing YAML frontmatter in ${absPath}`);
  }
  const { parse: parseYaml } = require(resolveFromApp('yaml'));
  const parsed = parseYaml(match[1]);
  if (!parsed || typeof parsed !== 'object') {
    throw new Error(`Invalid YAML frontmatter in ${absPath}`);
  }
  return parsed;
}

function isLikelyImagePath(urlPath) {
  return /\.(png|jpe?g|webp|gif|avif|bmp|tiff?)$/i.test(urlPath);
}

function toAbsolutePublicPath(urlPath) {
  const trimmed = String(urlPath || '').trim();
  if (!trimmed.startsWith('/')) return null;
  if (!trimmed.startsWith('/static/')) return null;
  return path.join(publicDir, trimmed.replace(/^\//, ''));
}

function toManifestModule(thumbs) {
  return `// Auto-generated by scripts/build-project-thumbnails.js. Do not edit.
export const generatedProjectThumbnails: Record<string, string> = ${JSON.stringify(thumbs, null, 2)};
`;
}

async function main() {
  const sharp = require(resolveFromApp('sharp'));
  const files = readProjectFiles(contentDir);
  const thumbsBySlug = {};
  const expectedNames = new Set();

  fs.mkdirSync(outputDir, { recursive: true });

  for (const absPath of files) {
    const raw = fs.readFileSync(absPath, 'utf8');
    let meta;
    try {
      meta = extractFrontmatter(raw, absPath);
    } catch (error) {
      process.stderr.write(`[build-project-thumbnails] skipping ${path.basename(absPath)}: ${error.message}\n`);
      continue;
    }

    const slug = String(meta.slug || '').trim();
    const thumbnail = String(meta.thumbnail || '').trim();
    const published = Boolean(meta.published);
    if (!published) continue;
    if (!slug || !thumbnail) continue;
    if (!isLikelyImagePath(thumbnail)) continue;

    const sourcePath = toAbsolutePublicPath(thumbnail);
    if (!sourcePath || !fs.existsSync(sourcePath)) continue;

    const outName = `${slug}.webp`;
    const outName2x = `${slug}@2x.webp`;
    const outPath = path.join(outputDir, outName);
    const outPath2x = path.join(outputDir, outName2x);
    expectedNames.add(outName);
    expectedNames.add(outName2x);

    try {
      await sharp(sourcePath)
        .resize({ width: THUMB_WIDTH, withoutEnlargement: true })
        .webp({ quality: THUMB_QUALITY })
        .toFile(outPath);
      await sharp(sourcePath)
        .resize({ width: THUMB_WIDTH_2X, withoutEnlargement: true })
        .webp({ quality: THUMB_QUALITY_2X })
        .toFile(outPath2x);
      thumbsBySlug[slug] = `/static/images/generated/project-thumbs/${outName}`;
    } catch (error) {
      process.stderr.write(`[build-project-thumbnails] failed ${slug}: ${error.message}\n`);
    }
  }

  if (fs.existsSync(outputDir)) {
    for (const name of fs.readdirSync(outputDir)) {
      if (!expectedNames.has(name)) {
        fs.rmSync(path.join(outputDir, name), { force: true });
      }
    }
  }

  fs.writeFileSync(manifestPath, toManifestModule(thumbsBySlug), 'utf8');
  process.stdout.write(
    `[build-project-thumbnails] generated ${Object.keys(thumbsBySlug).length} thumbnail(s)\n`,
  );
}

main().catch((error) => {
  process.stderr.write(`${error.stack || error}\n`);
  process.exit(1);
});
